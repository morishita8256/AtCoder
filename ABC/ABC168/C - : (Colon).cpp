#define M_PI 3.14159265358979

signed main() {
  double A, B, H, M;
  cin >> A >> B >> H >> M;

  double h12 = 12 * 60;
  double HM = H * 60 + M;

  double a = (2 * M_PI) * HM / h12;
  double b = (2 * M_PI) * M / 60;

  double c = abs(a - b);
  chmin(c, 2 * M_PI - c);
  double ans = sqrt(pow(B * sin(c), 2) + pow(B * cos(c) - A, 2));
  cout << ans << endl;
}