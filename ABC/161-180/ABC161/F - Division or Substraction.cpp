vector<ll> divisor(ll n) {
  vector<ll> ret;
  for (ll i = 1; i * i <= n; i++) {
    if (n % i == 0) {
      ret.push_back(i);
      if (i * i != n)
        ret.push_back(n / i);
    }
  }
  sort(begin(ret), end(ret));
  return (ret);
}

signed main() {
  int N;
  cin >> N;

  set<int> ok;

  auto div1 = divisor(N - 1);
  for (auto elem : div1) {
    if (elem == 1)
      continue;
    ok.insert(elem);
  }

  auto div2 = divisor(N);
  for (auto k : div2) {
    if (k == 1)
      continue;
    int N_cp = N;
    while (N_cp % k == 0)
      N_cp /= k;
    if (N_cp % k == 1)
      ok.insert(k);
  }

  int ans = len(ok);
  cout << ans << endl;
}